# Transform a number between 1 and 3 to its text name
# One, two three

number = int(input('Introduce a number between 1 to 3: '))

if number == 1:
    print('The number is One')
elif number == 2:
    print('The number is Two')
elif number == 3:
    print('The number is Three')
else:
    print('The number is invalid')
