# The operator overloading allows the same operator to have different meaning
# according to the context is called

# An example of operator overloading is "+"
# "+" Works different when used with numbers, strings, lists

# You can sum
a = 2
b = 3
print(a + b)

# You can concatenate
a = 'Hello '
b = 'World'
print(a + b)

# You can join
a = [1, 2, 3]
b = [4, 5, 6]
print(a + b)
