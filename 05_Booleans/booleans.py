'''
Eight ways to obtain a bool False in Python
1. Empty string ("", '')
2. Empty List []
3. Empty Tuple ()
4. Empty dictionary {}
5. Int Zero 0
6. Float Zero 0.0
7. bool False
8. None
'''

# A boolean can be True or False
my_variable = True
print(my_variable)

# Some operators (>, <, ...) returns a True or False
my_variable = 1 > 2
print(my_variable)
