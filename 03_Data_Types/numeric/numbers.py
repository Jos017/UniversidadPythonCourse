# Use type(data) to obtain the data type

# Numeric data is divided into three types

# 1. Integers (int)
int_numb = 1
print(int_numb, type(int_numb))

# 2. Float (float)
float_numb = 1.0
print(float_numb, type(float_numb))

# 3. Complex (int)
complex_numb = 1 + 1j
print(complex_numb, type(complex_numb))
