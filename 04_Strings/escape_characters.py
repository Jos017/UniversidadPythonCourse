# Escape characters
result = "This is a single quote(')"
print(result)
result = 'This is a single quote(\')'
print(result)

# Backspace (\b) removes a character
result = 'Letter g is going\b to be removed.'
print(result)

# next row
result = 'Letter g is a\nnext row.'
print(result)

# Directories windows
result = 'c:\\new\\juan'
print(result)

# row string (Ignores special characters just write as regular characters)
result = r'Letter g is a\nnext row.'
print(result)
