# You can define parameters in a function
# This mean that when you call a function
# you must provide arguments to execute the function

# Declare a function with params
def my_function(name, last_name):
    print(f'Saludos {name} {last_name}')


# Call a function with arguments
my_function('Jose', 'Rios')
my_function('Juan', 'Perez')
