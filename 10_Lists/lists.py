# A list in python is declared with square brackets []
names = ['Juan', 'Karla', 'Ricardo', 'Maria']

# A list can have items of different type
mixed_list = [1, 'Juan', True, ['hello', 'world']]

# Every element in the list has its index
# The index starts in 0
# You can access the items in a list by writing: list[index]
print(names[0]) # ==> Juan
print(names[1]) # ==> Karla
print(names[2]) # ==> Ricardo
print(names[3]) # ==> Maria
